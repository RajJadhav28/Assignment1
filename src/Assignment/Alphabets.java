package Assignment;

import java.util.Scanner;

public class Alphabets {

	public static void main(String[] args) {
		System.out.println("Alphabets form a-z:");
		for(int i=97;i<=122;i++) {
			System.out.print((char)i+" ");
		}
		System.out.println("\nAlphabets from A-Z:");
		for(int i=65;i<=90;i++) {
			System.out.print((char)i+" ");
		}
	}
}
